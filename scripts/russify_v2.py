EXCLUDE_DIRS = {'__pycache__', '.git', 'node_modules', '.venv', 'venv'}
EXCLUDE_FILES = {'russify.py', 'russify_v2.py'}


def main():
    root = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
    changed = 0
    for r, dirs, files in os.walk(root):
        dirs[:] = [d for d in dirs if d not in EXCLUDE_DIRS]
        for fn in files:
            if not fn.endswith('.py') or fn in EXCLUDE_FILES:
                continue
            p = os.path.join(r, fn)
            try:
                src = open(p, encoding='utf-8').read()
            except Exception:
                continue
            orig = src
            for cn, ru in TRANSLATIONS:
                if cn in src:
                    src = src.replace(cn, ru)
            if src != orig:
                with open(p, 'w', encoding='utf-8', newline='') as f:
                    f.write(src)
                changed += 1
                print(f'  [+] {os.path.relpath(p, root)}')
    print(f'\nИзменено: {changed}')


if __name__ == '__main__':
    main()
