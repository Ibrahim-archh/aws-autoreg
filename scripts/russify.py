EXCLUDE_DIRS = {'__pycache__', 'node_modules', '.git', '.venv', 'venv'}
EXCLUDE_FILES = {'russify.py'}


def translate_file(path: str) -> int:
    try:
        with open(path, encoding='utf-8') as f:
            src = f.read()
    except (UnicodeDecodeError, OSError):
        return 0

    orig = src
    for cn, ru in TRANSLATIONS:
        src = src.replace(cn, ru)

    if src != orig:
        with open(path, 'w', encoding='utf-8', newline='') as f:
            f.write(src)
        return 1
    return 0


def main():
    root = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
    changed = 0
    for r, dirs, files in os.walk(root):
        dirs[:] = [d for d in dirs if d not in EXCLUDE_DIRS]
        for fn in files:
            if not fn.endswith('.py') or fn in EXCLUDE_FILES:
                continue
            full = os.path.join(r, fn)
            if translate_file(full):
                rel = os.path.relpath(full, root)
                print(f"  [+] {rel}")
                changed += 1
    print(f"\nИзменено файлов: {changed}")


if __name__ == "__main__":
    main()
